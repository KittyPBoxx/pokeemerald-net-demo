class EasyChatHelper {

    static easyChatToText(arr) {
        let uint8Array = new Uint8Array([...arr]);
        let uint16Array = new Uint16Array(uint8Array.buffer)
        return [...uint16Array].map(c => words.get(EasyChatHelper.swap16(c))).join("");
    }

    static swap16(val) {
        return ((val & 0xFF) << 8)
               | ((val >> 8) & 0xFF);
    }
    
}

